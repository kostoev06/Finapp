package com.finapp.core.domain.result

import com.finapp.core.common.outcome.Outcome

/**
 * Результат «загрузки с фолбэком в кеш».
 *
 * - [data] — данные для отображения. Если ремоут отдал успех — это свежие данные;
 *   если ремоут провалился — это последнее, что лежит в локальном кеше (может быть
 *   пустым списком).
 * - [error] — `null`, если данные пришли с сервера. Не-null — если на ремоуте была
 *   ошибка и [data] прочитана из кеша; UI обычно показывает её снэкбаром.
 *
 * Контракт: даже при наличии [error] поле [data] всегда осмысленно — VM может
 * безусловно отрендерить его в стейт.
 */
data class LoadResult<out T>(
    val data: T,
    val error: Outcome.FailureOutcome? = null
)
