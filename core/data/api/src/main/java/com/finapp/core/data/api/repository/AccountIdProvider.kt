package com.finapp.core.data.api.repository

/**
 * Поставщик идентификатора текущего аккаунта.
 *
 * Приложение пока однопрофильное — id зашит на этапе сборки. Интерфейс существует, чтобы
 * репозитории не дёргали `BuildConfig` напрямую: при появлении мульти-аккаунта или
 * выбора профиля заменим реализацию (например, на чтение из DataStore), а контракт
 * репозиториев останется прежним.
 */
interface AccountIdProvider {
    fun get(): Long
}
